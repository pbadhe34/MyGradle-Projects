 package app;
 	 	
 
 public class InclusionException extends Exception
 	 	
 {
 	 	
     public InclusionException( String message )
 	 	
     {
  	 	
         super( message );
 	 	
     }
  
 	 	
     public InclusionException( String message, Throwable cause )
 	 	
     {
 	  	
         super( message, cause );
 	 	
     }
 	 	
 }